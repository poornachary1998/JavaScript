// Type Conversion: When you manually convert a value from one type to another type is called Type conversion.

//To String:
console.log(String(123)); //123

(123).toString(); //123

//To Number:
Number("122"); //122
Number("abc"); //NaN
parseInt("14px"); //14
parseFloat("3.14") //3.14

//To Boolean:
Boolean(1); // true
Boolean(0); //false
Boolean(""); //false
Boolean("hello") // true;

